/**
 * @param {number} num
 * @return {boolean}
 */
var checkPerfectNumber = function(num) {
    // 0 یا اس سے چھوٹے نمبر پرفیکٹ نہیں ہو سکتے
    if (num <= 0) return false;

    let sum = 0;
    // ہم صرف اسکوائر روٹ تک جائیں گے تاکہ ٹائم بچے
    for (let i = 1; i * i <= num; i++) {
        if (num % i === 0) {
            sum += i;
            // اگر i نمبر خود نہیں ہے اور i*i بھی نمبر نہیں ہے (تاکہ ڈپلیکیٹ نہ ہو)
            if (i * i !== num && i !== 1) {
                sum += num / i;
            }
        }
    }

    // مجموعہ نمبر کے برابر ہونا چاہیے (اور 1 کو ہم نے شروع میں ہینڈل کر لیا ہے)
    // نوٹ: چونکہ ہم نے 1 کو شامل کیا ہے، لیکن فارمولے میں خود نمبر شامل نہیں ہونا چاہیے
    // تو اگر مجموعہ نمبر کے برابر ہے اور نمبر 1 نہیں ہے (کیونکہ 1 کا کوئی ڈیوائزر نہیں)
    return sum === num && num !== 1;
};